
function saveAndCheckPassword28() {
  const passwordInput5 = document.getElementById("passwordteather5"); 
 const textInput28 = document.getElementById("myInput28");   
 const password28 = passwordInput5.value;
 const text28 = textInput28.value;
 // التحقق من كلمة المرور لحفظ النص    
 if (password28 === "op5") {    
 // الحصول على النصوص المحفوظة أو إنشاء مصفوفة جديدة
 let savedTexts28 = JSON.parse(localStorage.getItem("savedTexts28")) || [];    
 savedTexts28.push(text28);
 localStorage.setItem("savedTexts28", JSON.stringify(savedTexts28));        
    // عرض النصوص المحفوظة
    displaySavedTexts28();
 } else {
 alert("كلمة المرور خاطئة");
 }
 }
 // دالة لعرض النصوص المحفوظة
 function displaySavedTexts28() {
 const resultDiv28 = document.getElementById("result28");
 const savedTexts28 = JSON.parse(localStorage.getItem("savedTexts28")) || [];
 resultDiv28.innerHTML = ""; // مسح المحتوى السابق
 savedTexts28.forEach(text28 => {
 const newParagraph28 = document.createElement("p");
 newParagraph28.textContent = text28;
 resultDiv28.appendChild(newParagraph28);
 });
 }

 
