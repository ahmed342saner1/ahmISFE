
function saveAndCheckPassword21() {
  const passwordInput4 = document.getElementById("passwordteather4"); 
 const textInput21 = document.getElementById("myInput21");   
 const password21 = passwordInput4.value;
 const text21 = textInput21.value;
 // التحقق من كلمة المرور لحفظ النص    
 if (password21 === "ui4") {    
 // الحصول على النصوص المحفوظة أو إنشاء مصفوفة جديدة
 let savedTexts21 = JSON.parse(localStorage.getItem("savedTexts21")) || [];    
 savedTexts21.push(text21);
 localStorage.setItem("savedTexts21", JSON.stringify(savedTexts21));        
    // عرض النصوص المحفوظة
    displaySavedTexts21();
 } else {
 alert("كلمة المرور خاطئة");
 }
 }
 // دالة لعرض النصوص المحفوظة
 function displaySavedTexts21() {
 const resultDiv21 = document.getElementById("result21");
 const savedTexts21 = JSON.parse(localStorage.getItem("savedTexts21")) || [];
 resultDiv21.innerHTML = ""; // مسح المحتوى السابق
 savedTexts21.forEach(text21 => {
 const newParagraph21 = document.createElement("p");
 newParagraph21.textContent = text21;
 resultDiv21.appendChild(newParagraph21);
 });
 }

 
