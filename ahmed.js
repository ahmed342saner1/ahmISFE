
function saveAndCheckPassword7() {
    const passwordInput2 = document.getElementById("passwordteather2"); 
   const textInput7 = document.getElementById("myInput7");   
   const password7 = passwordInput2.value;
   const text7 = textInput7.value;
   // التحقق من كلمة المرور لحفظ النص    
   if (password7 === "er2") {    
   // الحصول على النصوص المحفوظة أو إنشاء مصفوفة جديدة
   let savedTexts7 = JSON.parse(localStorage.getItem("savedTexts7")) || [];    
   savedTexts7.push(text7);
   localStorage.setItem("savedTexts7", JSON.stringify(savedTexts7));        
      // عرض النصوص المحفوظة
      displaySavedTexts7();
   } else {
   alert("كلمة المرور خاطئة");
   }
   }
   // دالة لعرض النصوص المحفوظة
   function displaySavedTexts7() {
   const resultDiv7 = document.getElementById("result7");
   const savedTexts7 = JSON.parse(localStorage.getItem("savedTexts7")) || [];
   resultDiv7.innerHTML = ""; // مسح المحتوى السابق
   savedTexts7.forEach(text7 => {
   const newParagraph7 = document.createElement("p");
   newParagraph7.textContent = text7;
   resultDiv7.appendChild(newParagraph7);
   });
   }

   
