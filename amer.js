
function saveAndCheckPassword14() {
    const passwordInput3 = document.getElementById("passwordteather3"); 
   const textInput14 = document.getElementById("myInput14");   
   const password14 = passwordInput3.value;
   const text14 = textInput14.value;
   // التحقق من كلمة المرور لحفظ النص    
   if (password14 === "ty3") {    
   // الحصول على النصوص المحفوظة أو إنشاء مصفوفة جديدة
   let savedTexts14 = JSON.parse(localStorage.getItem("savedTexts14")) || [];    
   savedTexts14.push(text14);
   localStorage.setItem("savedTexts14", JSON.stringify(savedTexts14));        
      // عرض النصوص المحفوظة
      displaySavedTexts14();
   } else {
   alert("كلمة المرور خاطئة");
   }
   }
   // دالة لعرض النصوص المحفوظة
   function displaySavedTexts14() {
   const resultDiv14 = document.getElementById("result14");
   const savedTexts14 = JSON.parse(localStorage.getItem("savedTexts14")) || [];
   resultDiv14.innerHTML = ""; // مسح المحتوى السابق
   savedTexts14.forEach(text14 => {
   const newParagraph14 = document.createElement("p");
   newParagraph14.textContent = text14;
   resultDiv14.appendChild(newParagraph14);
   });
   }

   
