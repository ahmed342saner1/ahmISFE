
function saveAndCheckPassword35() {
  const passwordInput6 = document.getElementById("passwordteather6"); 
 const textInput35 = document.getElementById("myInput35");   
 const password35 = passwordInput6.value;
 const text35 = textInput35.value;
 // التحقق من كلمة المرور لحفظ النص    
 if (password35 === "as6") {    
 // الحصول على النصوص المحفوظة أو إنشاء مصفوفة جديدة
 let savedTexts35 = JSON.parse(localStorage.getItem("savedTexts35")) || [];    
 savedTexts35.push(text35);
 localStorage.setItem("savedTexts35", JSON.stringify(savedTexts35));        
    // عرض النصوص المحفوظة
    displaySavedTexts35();
 } else {
 alert("كلمة المرور خاطئة");
 }
 }
 // دالة لعرض النصوص المحفوظة
 function displaySavedTexts35() {
 const resultDiv35 = document.getElementById("result35");
 const savedTexts35 = JSON.parse(localStorage.getItem("savedTexts35")) || [];
 resultDiv35.innerHTML = ""; // مسح المحتوى السابق
 savedTexts35.forEach(text35 => {
 const newParagraph35 = document.createElement("p");
 newParagraph35.textContent = text35;
 resultDiv35.appendChild(newParagraph35);
 });
 }

 
